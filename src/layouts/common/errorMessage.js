const { toast } = require("react-toastify");

const handleErrorMessage = (message) =>{
  toast.error(message, {
    position: "bottom-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
  });
}

export default handleErrorMessage
